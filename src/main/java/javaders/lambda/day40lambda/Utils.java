package javaders.lambda.day40lambda;
/** gerekli methodlari icerir. Utils Class Mesela String bir datanin karakter sayisinin karesini al yok.
    Kendi Methodunu kendin yap */


public class Utils {
    public static int karesiniAlMethodu(String s){
        return s.length() * s.length();
    }
    public static boolean ciftMi(String s){
        return s.length() % 2 == 0;
    }





}
